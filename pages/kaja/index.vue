        <h2>Unsworn Shaman Class Features</h2>
        <p id="minor-spirit"><strong>Minor Spirit:</strong> An unsworn shaman forms a temporary bond with a minor spirit, gaining three shaman, witch or wandering spirit hexes of her choosing for the day.</p>
        <p id="hex"><strong>Hex:</strong> An unsworn shaman learns hexes through her minor spirit ability. Using a hex is a standard action that doesn't provoke an attack of opportunity unless otherwise noted. The saving throw DC to resist a hex is equal to 10 + 1/2 the shaman's level + the shaman's Wisdom modifier.</p>
        <p id="spirit-animal"><strong>Spirit Animal:</strong> A shaman must commune with her spirit animal each day to prepare spells. A spirit animal uses the rules for a wizard's familiar, except that it gains an additional ability from one of the unsworn shaman's wandering spirits chosen when she prepares spells and is treated as an outsider with the native subtype. A shaman's spirit animal can deliver hexes in addition to touch spells.</p>
        <p id="spirit-magic"><strong>Spirit Magic:</strong> A shaman can spontaneously cast a limited number of spells per day beyond those she prepared ahead of time. She has one spell slot per day of each shaman spell level she can cast, not including orisons. She can choose these spells from the list of spells granted by her wandering spirits at the time she casts them. She can enhance these spells using any metamagic feat that she knows, using up a higher-level spell slot as required by the feat and increasing the time to cast the spell.</p>
        <p id="wandering-spirit"><strong>Wandering Spirit:</strong> An unsworn shaman can temporarily bond with two spirits per day, gaining spirit magic spells and spirit abilities.</p>
