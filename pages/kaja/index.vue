        <h2>Background</h2>
        <p>Kaja was raised in Cesca, a hilltop community surrounded in the vineyard-covered rolling hills of Varno county in eastern Ustalav. Her parents are ordinary townsfolk. They noticed the subtle hints of her aasimar heritage but had no clue where this ancestry descended from in their family tree. She grew in the faith of Pharasma in Cesca's local Pharasmin temple, Whisperwall, placing votive candles in the wall’s hollows in remembrance of family members who had passed on. Here she was first intrigued by townsfolk speaking with spirits of the dead, although she wouldn’t learn how to do so herself until much later in life.</p>
        <p>When she reached adulthood, Kaja was invited to study at the famed Lepidstadt University. She accepted the invitation and completed her studies there under the famed Professor Lorrimor. The experience opened her eyes to a wide world of knowledge, both public and hidden. She was one of his top students, and accompanied him on many research projects. Her drive to collect hidden information attracted the attention of local members of the Esoteric Order of the Palatine Eye. After her studies were completed, they offered her an apprenticeship.</p>
        <p>Kaja was ecstatic to be invited to join an organization that she had only heard rumors about, and enthusiastically absorbed their teachings. Membership in the order granted her access to vaults of occult knowledge, and she was taught how to draw upon spiritual energy found throughout the land. Missions on behalf of the order brought her to nearly every corner of the nation, racing to find secrets before they were stolen by even more mysterious competitors.</p>
        <p>Kaja has become a devoted member of the organization, and after years of loyal service in the Third Throne she would like to climb into the higher tiers to learn what secrets they hold. She knows this will be difficult without having a noble title. She doesn’t believe the order to be an altruistic organization, but it would take some truly shocking information to turn her against it.</p>
        <hr/>
        <p>As an unsworn shaman, Kaja can call upon two different spirits each day, drawing power from the forces they represent. Her connection to each is noted below.</p>
        <p><strong>Ancestors:</strong> The reign of Soividia Ustav’s line was a golden age, before the rise of the Whispering Tyrant turned the kingdom into a realm of darkness. In the ancient royal burial ground of Evercrown, Kaja learned to invoke spirits from the early days of the nation to call on the lost grandeur of that age.</p>
        <p><strong>Battle:</strong> The Shining Crusade brough heroes from across the Inner Sea to end the threat of the Whispering Tyrant. Kaja studied sites of ancient battles in Amaans from that long campaign and learned to call on the spirits of those heroes for strength.</p>
        <p><strong>Bones:</strong> Six centuries under the Whispering Tyrant’s rule changed Ustalav forever. Lingering necromantic energy is prevalent throughout the nation. Through her faith in Pharasma, Kaja has learned to call upon this energy to create terrifying effects.</p>
        <p><strong>Dark Tapestry:</strong> Kaja has spent her adult life scouring the lands of Ustalav for hidden occult secrets. She found many of these in quiet communities near Lake Encarthan where locals have turned to terrifying powers. Calling on these spirits might be dangerous, but useful in the right circumstances.</p>
        <p><strong>Flame:</strong> The trench warfare and subsequent scorched earth policy employed by Count Neska in the civil war over the Furcina Plain ravaged the land and created the Furrows - a land coated in ash and cinders. Kaja traveled to the plain to connect with spirits of flame left over from that terrible conflict, and can still upon them when the situation requires.</p>
        <p><strong>Frost:</strong> In the dead of winter, Ustalav’s high mountains are white with snow, and cold winds pass through its valleys and streams freeze over. Kaja has learned to channel the spirits of Ustalav’s harsh winters.</p>
        <p><strong>Heavens:</strong> In Vieland, the secluded northwestern corner of Ustalav, ancient Kellids raised strange rune-covered monuments to the power of the distant stars. This was one of the first places Kaja learned to draw upon spirits from Ustalav’s past after her studies under Professor Lorrimor at the nearby University of Lepidstadt.</p>
        <p><strong>Life:</strong> If she had not found her calling among the Esoteric Order, Kaja would likely have become a priestess of Pharasma. She studied with Pharasmin clerics in the gray cathedral of Velonstair, and learned how to use spirits of life to emulate some of their talents.</p>
        <p><strong>Lore:</strong> Professor Lorrimor instilled in Kaja a lifelong love of learning, and of ferreting out lost knowledge. The lore spirits she bonded with at University of Lepidstadt are her oldest and most reliable partners. With their aid she can call upon the arcane powers of Lorrimor and others from Ustalav’s scholarly history.</p>
        <p><strong>Mammoth:</strong> At the Quarterfax Archives museum in Caliphas, Kaja first saw bones of mammoth megafauna from Ustalav’s distant past. She learned how to commune with the remnant spirits of these great beasts.</p>
        <p><strong>Nature:</strong> Kaja spent time with the vale folk in the Hungry Mountains, quiet communities who seek not to offend the nature spirits, fairies, hags, and dragons that surround them. Kaja encountered several of these creatures peacefully and learned to invoke their powers.</p>
        <p><strong>Slums:</strong> Once the capital of Ustalav, Ardis fell into disrepair after the royal court was relocated to Caliphas. In once-elegant manors now inhabited by rats and squatters, Kaja learned to bond with spirits of the downtrodden.</p>
        <p><strong>Stone:</strong> Bleakwall, an uneven wall of stones and fallen fortresses spans the ninety miles of border between Ustalav and the orc hold of Belkzen. While most of it is crumbling, Kaja learned to call on the spirits here for their mastery of stone.</p>
        <p><strong>Tribe:</strong> Ustalav was once a land populated by tribes of Kellids. Years ago Kaja studied among some of their descendents, the “swampers” around Lake Prophyria to learn some of the mystic traditions passed down from their barbarian ancestors.</p>
        <p><strong>Waves:</strong> The dark murky waters of Lake Encarthan loom large in the imagination of many Ustavs. Kaja learned to bond with spirits found in its stormy waters on fishing boats plying Avalon Bay.</p>
        <p><strong>Wind:</strong> High among the peaks of the eastern Hungry Mountains, fierce winds and terrible storms tear through the skies. Kaja visited these peaks to learn to harness spirits of wind.</p>
        <p><strong>Wood:</strong> The Shudderwood is one of the largest forests in northern Avistan. Kaja visited Ascanor Lodge and learned to call upon spirits of wood under the canopy of its ancient looming trees.</p>
